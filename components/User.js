import User from "../model/User.js";

//CREATE NEW USER
export const newUser = async (req, res, next) => {
  try {
    const user = new User({ ...req.body.user });

    const savedUser = await user.save();

    if (savedUser) {
      return res.status(200).send("Ok");
    }
  } catch (error) {
    return res.status(200).send("error");
  }
};

